package tech.fastj.graphics.display;

/**
 * {@link Display} states.
 *
 * @author Andrew Dey
 * @since 1.6.0
 */
public enum DisplayState {
    /** The {@link Display display} is windowed. */
    Windowed,
    /** The {@link Display display} is iconified. */
    Iconified,
    /** The {@link Display display} is borderless. */
    Borderless,
    /** The {@link Display display} is full-screen. */
    FullScreen
}
